#!/bin/bash

# --------------------------------------
# Script to convert antiX16 to LegacyIce
# --------------------------------------

# (Only GTK and Xfce application)
# (In alphabetical order)

### Set background
echo -e "\e[40m\n\n"

### Velcome
echo -e "\e[93m----------------------------------------------------------\e[38;5;46m"
echo -e "\e[93m Collect scripts for LegacyIce by KERNEL ULTRAS                  \e[38;5;46m"
echo -e "\e[93m----------------------------------------------------------\e[38;5;46m\n"

########################################################################################
### Copy IceWM config files
########################################################################################
echo -e "\e[93m Collect IceWM config files\e[38;5;46m\n"
cp -r ~/.icewm antiX-20/configs/
#***************************************************************************************

########################################################################################
### Copy .bashrc
########################################################################################
echo -e "\e[93m Collect BASH config file\e[38;5;46m\n"
cp ~/.bashrc antiX-20/configs/.bashrc
#***************************************************************************************

########################################################################################
### Copy gtkrc-2.0
########################################################################################
echo -e "\e[93m Collect GTK-2 config file\e[38;5;46m\n"
cp ~/.gtkrc-2.0 antiX-20/configs/.gtkrc-2.0
#***************************************************************************************

########################################################################################
### Copy gtk-3.0
########################################################################################
echo -e "\e[93m Collect GTK-3 config file\e[38;5;46m\n"
cp -r ~/.config/gtk-3.0 antiX-20/configs/
#***************************************************************************************

########################################################################################
### Copy Compton config
########################################################################################
echo -e "\e[93m Collect Compton config file\e[38;5;46m\n"
cp ~/.config/compton.conf antiX-20/configs/compton.conf
#***************************************************************************************

########################################################################################
### Copy Xfce4 config files
########################################################################################
echo -e "\e[93m Collect Xfce4 config files\e[38;5;46m\n"
cp -r ~/.config/xfce4 antiX-20/configs/
#***************************************************************************************

### END OF SCRIPT
echo -e "\n"
echo -e "\e[93m##################################################\e[38;5;46m\n"
echo -e "\e[93m##################################################\e[38;5;46m\n"
echo -e "\n"
echo -e "\e[93m- All is OK\e[38;5;46m\n"
echo -e "\e[0m\n"
